import java.util.Scanner;
public class Pb17
{
    public static void main(String[] args) 
    { 
        Scanner sc = new Scanner (System.in);
        System.out.println("Please Enter Triangle Height: ");
        int size = sc.nextInt();
        for(int lineCount=1; lineCount<=size; ++lineCount)
        {
            for(int numCount=1; numCount<=size; ++numCount)
            {
                if(numCount==1 || lineCount==size || lineCount==numCount)
                {
                    System.out.print(numCount);
                }
                else{System.out.print(" ");}
            }
            System.out.println();
        }
    }
}

